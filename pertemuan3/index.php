<?php
  // Pertemuan 3 - Struktur Kendali

  // Pengulangan
  // 1. For
  for($i = 0; $i < 5; $i++){
    echo "For Loop <br>";
    }

  // 2. While
  $i = 0;
  while($i < 5){
    echo "While Loop <br>";
    $i ++;
    }
    
  // 3. Do while
  $i = 0;
  do{
    echo "Do while Loop <br>";
    $i ++;
    } while($i < 5);
?>